def write_text_utf8_bom(path: Path, content: str) -> None:
    path.write_text(content, encoding="utf-8-sig")


def section(section_id: str, heading: str, body: str) -> str:
    return dedent(
        f"""\
        <section class="content-box" id="{section_id}">
          <h2>{heading}</h2>
        {indent(body.strip(), 2)}
        </section>
        """
    )


def indent(text: str, level: int) -> str:
    prefix = "  " * level
    return "\n".join(prefix + line if line else line for line in text.splitlines())


def render_summary(items: list[str]) -> str:
    lis = "\n".join(f"    <li>{item}</li>" for item in items)
    return f"<ul class=\"summary-list\">\n{lis}\n  </ul>"


def render_paragraphs(paragraphs: list[str]) -> str:
    return "\n".join(f"<p>{paragraph}</p>" for paragraph in paragraphs)


def render_list(items: list[str]) -> str:
    lis = "\n".join(f"  <li>{item}</li>" for item in items)
    return f"<ul>\n{lis}\n</ul>"


def render_ol(items: list[str]) -> str:
    lis = "\n".join(f"  <li>{item}</li>" for item in items)
    return f"<ol>\n{lis}\n</ol>"


def render_photo_slot(label: str) -> str:
    return f'<div class="photo-slot">{html.escape(label)}</div>'


def render_time_example(title: str, intro: str, rows: list[tuple[str, str, str]], total: str) -> str:
    row_html = "\n".join(
        dedent(
            f"""\
            <div class="time-row">
              <b>{when}</b>
              <span>{what}</span>
              <span>{duration}</span>
            </div>
            """
        ).strip()
        for when, what, duration in rows
    )
    return dedent(
        f"""\
        <div class="time-example">
          <h3>{title}</h3>
          <p>{intro}</p>
          {row_html}
          <div class="time-total">
            <span>移動時間の目安</span>
            <span>{total}</span>
          </div>
        </div>
        """
    ).strip()


def render_spot_cards(spots: list[dict[str, str]]) -> str:
    cards = []
    for spot in spots:
        note = f'<div class="spot-note">{spot["note"]}</div>' if spot.get("note") else ""
        meta = []
        if spot.get("address"):
            meta.append(f'<p><b>住所：</b>{spot["address"]}</p>')
        if spot.get("phone"):
            meta.append(f'<p><b>電話番号：</b>{spot["phone"]}</p>')
        if spot.get("url"):
            meta.append(
                f'<p><b>公式サイト：</b><a href="{spot["url"]}" target="_blank" rel="noopener">公式サイト</a></p>'
            )
        meta_block = (
            '<div class="hotel-contact">\n'
            + indent("\n".join(meta), 1)
            + "\n</div>"
            if meta
            else ""
        )
        cards.append(
            dedent(
                f"""\
                <article class="spot-card">
                  <div class="spot-photo">{spot["photo_label"]}</div>
                  <div>
                    <h3>{spot["title"]}</h3>
                    <p>{spot["body1"]}</p>
                    <p>{spot["body2"]}</p>
                    {meta_block}
                    {note}
                  </div>
                </article>
                """
            ).strip()
        )
    return '<div class="spot-grid">\n' + indent("\n".join(cards), 1) + "\n</div>"


def render_affiliate(cards: list[dict[str, str]]) -> str:
    items = []
    for card in cards:
        klass = "button secondary" if card.get("secondary") else "button"
        items.append(
            dedent(
                f"""\
                <div class="affiliate-card">
                  <b>{card["title"]}</b>
                  <p>{card["body"]}</p>
                  <a class="{klass}" href="#">{card["cta"]}</a>
                </div>
                """
            ).strip()
        )
    return '<div class="affiliate-grid" aria-label="予約導線">\n' + indent("\n".join(items), 1) + "\n</div>"




def build_article_main(article: dict) -> str:
    fit_body = "\n".join(
        [render_paragraphs(article["fit_paragraphs"]), render_list(article["fit_list"])]
    ).strip()
    course_parts = [
        render_photo_slot(article["course_photo_label"]),
        render_paragraphs(article["course_paragraphs"]),
    ]
    if article.get("time_example"):
        course_parts.append(
            render_time_example(
                article["time_example"]["title"],
                article["time_example"]["intro"],
                article["time_example"]["rows"],
                article["time_example"]["total"],
            )
        )
    course_parts.extend(
        [
            "<h3>1日目のイメージ</h3>",
            render_ol(article["day1"]),
            "<h3>2日目のイメージ</h3>",
            render_ol(article["day2"]),
            render_paragraphs(article["course_note"]),
        ]
    )

    hotel_parts = [render_paragraphs(article["hotel_paragraphs"]), render_list(article["hotel_list"])]
    if article.get("hotel_spots"):
        hotel_parts.extend(["<h3>宿の候補</h3>", render_spot_cards(article["hotel_spots"])])

    booking_parts = [render_paragraphs(article["booking_paragraphs"])]
    for sub in article["booking_sections"]:
        booking_parts.append(f"<h3>{sub['title']}</h3>")
        booking_parts.append(render_paragraphs(sub["paragraphs"]))
    if article.get("booking_list"):
        booking_parts.append(render_list(article["booking_list"]))
    booking_parts.append(render_affiliate(article["affiliate_cards"]))

    sections = [
        section("summary", "ひとことでいうと", render_summary(article["summary"])),
        section("fit", article["fit_heading"], fit_body),
        section("course", "1泊2日で大丈夫？", "\n".join(course_parts)),
        section("carfree", "レンタカーは必要？", render_paragraphs(article["carfree_paragraphs"])),
        section("hotel", "宿選びのポイント", "\n".join(hotel_parts)),
        section(
            "spots",
            article["spots_heading"],
            "\n".join([render_paragraphs(article["spots_intro"]), render_spot_cards(article["spots_cards"])]),
        ),
        section("booking", "予約前に確認したいこと", "\n".join(booking_parts)),
    ]
    return '<article class="article-main">\n' + indent("\n\n".join(sections), 1) + "\n</article>"


def build_aside(article: dict) -> str:
    labels = article.get(
        "toc_labels",
        [
            ("summary", "ひとことでいうと"),
            ("fit", "向いている旅"),
            ("course", "1泊2日で大丈夫？"),
            ("carfree", "レンタカー"),
            ("hotel", "宿選び"),
            ("spots", "立ち寄り候補"),
            ("booking", "予約前の確認"),
        ],
    )
    links = "\n".join(f'  <a href="#{anchor}">{label}</a>' for anchor, label in labels)
    return dedent(
        f"""\
        <aside class="side-box" aria-label="記事目次">
          <h2>この記事の内容</h2>
        {links}
          <div class="article-dates">
            <span><b>公開日</b>{article["published"]}</span>
            <span><b>更新日</b>{article["updated"]}</span>
          </div>
        </aside>
        """
    )


